/*
Переменные - это именнованное хранинилише данных. Переменные начинаются с ключевого
слова let. Переменные инициализируются в начале программы. Далее мы можем 
поменять старые данные на новые. При этом старые данные стираются. 
Константы - это неизменяемые переменные. После инициализации, константу
нельзя поменять.
*/

let name_var = "Hello World";
name_var = "Привет Мир";
console.log(name_var);
const PI_NUM = 3.14159265;
console.log(PI_NUM);